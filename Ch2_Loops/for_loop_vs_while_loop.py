#while loop
my_var = 100
while my_var <= 200:
    print(my_var)
    my_var += 2

#converting while loop to for loop
for my_var in range(100, 201, 2):
    print(my_var)

#for loop
for i in range(10, 5, -1):
    print(i)

#converting for loop for while loop
i = 10
while i > 5:
    print(i)
    i -= 1
